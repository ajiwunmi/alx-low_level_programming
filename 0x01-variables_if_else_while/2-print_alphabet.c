#include <stdio.h>
#include <stdlib.h>
#include <time.h>

/**
 * main -Entry point
 *
 * Description: 'Checks a random number if positive, negative or Zero'
 *
 * Return: return 0 to indicate end of program
 */
int main(void)
{
	char alphabet;
	for (alphabet = 'a'; alphabet <= 'z'; alphabet++)
	{

		putchar(alphabet);
	}
	putchar('\n');
	return (0);
}
